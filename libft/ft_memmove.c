#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	int		i;
	char	*s1;
	char	*s2;

	if (!dest || !src)
		return (NULL);
	s1 = (char *)src;
	s2 = (char *)dest;
	if (dest > src)
	{
		i = (int)(n);
		while (i >= 0)
		{
			s2[i] = s1[i];
			i--;
		}
	}
	else
		ft_memcpy(dest, src, n);
	return (dest);
}

